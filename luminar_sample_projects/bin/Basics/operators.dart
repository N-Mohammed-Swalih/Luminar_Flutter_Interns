void main() {
  // 1.arithematic operators
  dynamic a = 10, b = 3;
  print("The sum is : ${a + b}");
  print("The differance is : ${a - b}");
  print("The product is : ${a * b}");
  print("The division is : ${a / b}");
  print("The tilt division is : ${a ~/ b}");
  print("The modulus is ${a % b}");

//2.assignment operators

  print("a=b ${a = b}");
  print("a+=b ${a += b}");
  print("a-=b ${a -= b}");
  print("a*=b ${a *= b}");
  print("a/=b ${a /= b}");
}
